-- ============================================================
-- SYNC IDEA VOTES COUNT
-- ============================================================
create or replace function public.sync_idea_votes_count()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    update public.ideas set votes_count = votes_count + 1 where id = new.idea_id;
    return new;
  elsif tg_op = 'DELETE' then
    update public.ideas set votes_count = greatest(votes_count - 1, 0) where id = old.idea_id;
    return old;
  end if;
  return null;
end;
$$;

create trigger idea_votes_count_trigger
  after insert or delete on public.idea_votes
  for each row execute function public.sync_idea_votes_count();

