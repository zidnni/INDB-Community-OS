-- ============================================================
-- PROFILES
-- ============================================================
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  username text unique,
  avatar_url text,
  bio text,
  city text,
  role text not null default 'member' check (role in ('member', 'contributor', 'historian', 'moderator', 'admin')),
  language_preference text not null default 'ar',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- ============================================================
-- CATEGORIES (multi-locale)
-- ============================================================
create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name_en text not null,
  name_fr text not null,
  name_ar text not null,
  slug text not null unique,
  icon text,
  color text,
  created_at timestamptz not null default now()
);

-- ============================================================
-- POSTS
-- ============================================================
create table if not exists public.posts (
  id uuid primary key default gen_random_uuid(),
  author_id uuid references public.profiles(id) on delete set null,
  category_id bigint references public.categories(id) on delete set null,
  type text not null default 'community' check (type in ('community', 'news', 'memory', 'event', 'idea', 'project')),
  title text,
  content text not null,
  image_url text,
  status text not null default 'published' check (status in ('published', 'hidden', 'archived')),
  language text not null default 'auto',
  likes_count integer not null default 0,
  comments_count integer not null default 0,
  saves_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- ============================================================
-- COMMENTS (supports replies via parent_id)
-- ============================================================
create table if not exists public.comments (
  id uuid primary key default gen_random_uuid(),
  post_id uuid not null references public.posts(id) on delete cascade,
  author_id uuid references public.profiles(id) on delete set null,
  parent_id uuid references public.comments(id) on delete cascade,
  content text not null,
  status text not null default 'published' check (status in ('published', 'hidden')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- ============================================================
-- POST LIKES
-- ============================================================
create table if not exists public.post_likes (
  id uuid primary key default gen_random_uuid(),
  post_id uuid not null references public.posts(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
